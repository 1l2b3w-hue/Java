public class char01 {

	public static void main(String[] args) {
		char a = 'a';
		char b = '\t';
		char c = '值';
		char d = 97;
		char e = 0x41;
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		System.out.println(d);
		System.out.println(e);
		// ctrl + shift + d : 实现复制该行并粘贴到下一行
		// ctrl + shift + k : 删除整行
	}
}